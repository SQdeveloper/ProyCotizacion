    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        if(cmbProducto.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Complete todos los datos de su compra.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else{
            Cotizacion cot = new Cotizacion(cmbProducto.getSelectedItem().toString(), (String)cmbCant.getSelectedItem(), txtPrecio.getText(), txtInteres.getText(), lbPrecioFinal.getText());
            lista.add(cot);

            c1.Escritura_BD(cot, bd1.getSt());
        }
    }//GEN-LAST:event_btnComprarActionPerformed

    public void precio_Final(){
        String cantidad = (String)cmbCant.getSelectedItem();
        int prec = 0;
        
        prec = Integer.parseInt(txtPrecio.getText().replace("$",""))*Integer.parseInt(cantidad);
  
        double pfinal =  prec - (prec*0.18);
        txtPrecio.setText(prec +"$");
        lbPrecioFinal.setText("$" +pfinal);
        pfinal = 0;
    }
    
