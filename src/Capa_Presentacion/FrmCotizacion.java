    private void cmbProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbProductoActionPerformed
        if(cmbProducto.getSelectedItem().equals("-------------Seleccionar--------------")){
            txtPrecio.setText("0");
        }
        
        else if(cmbProducto.getSelectedItem().equals("Ryzen 5 5600G")){
            txtPrecio.setText("$1050");
        }
        else if(cmbProducto.getSelectedItem().equals("Case Iron gtx")){
            txtPrecio.setText("$450");
        }
        else if(cmbProducto.getSelectedItem().equals("GFORCE RTX 5000")){
            txtPrecio.setText("$3650");
        }
        else if(cmbProducto.getSelectedItem().equals("Samsung 32 pulgadas")){
            txtPrecio.setText("$2180");
        }
        else if(cmbProducto.getSelectedItem().equals("Mause Logitech")){
            txtPrecio.setText("$345");
        }
        precio_Final();
    }//GEN-LAST:event_cmbProductoActionPerformed

